// Custom comparator
struct cmp
{
    bool operator()(const pair<int, int> &p1, const pair<int, int> &p2) const
    {
        if (p1.second == p2.second)
        {
            return p1.first < p2.first; // If second elements are equal, compare first
        }
        return p1.second < p2.second; // Otherwise, compare second elements
    }
};

void solve()
{
    int n, k;
    cin >> n >> k;
    vector<pi> v(n);
    cin >> v;
    sort(v.begin(), v.end(), cmp());
    multiset<int> s;
    for (int i = 0; i < k; i++)
    {
        s.insert(0);
    }
    int ans = 0;
    // 5 0
    for (int i = 0; i < n; i++)
    {
        auto it = s.upper_bound(v[i].first);
        if (it != s.begin())
        {
            it--;
        }
        if (*it <= v[i].first)
        {
            s.erase(it);
            s.insert(v[i].second);
            ans++;
        }
    }
    cout << ans << endl;
}

